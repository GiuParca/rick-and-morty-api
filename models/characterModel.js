export default class CharacterModel {

    constructor(id, name, status, species, gender, image, url, locationName){
        this.id = id;
        this.name = name;
        this.status = status;
        this.species = species;
        this.gender = gender;
        this.image = image;
        this.url = url;
        this.locationName = locationName;
    }

}
